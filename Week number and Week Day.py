week_num= int(input("Enter the day No in Week(1-7):"))

if week_num==1:
    print("1 is Monday")
elif week_num==2:
    print("2 is Tuesday")
elif week_num==3:
    print("3 is Wednesday")
elif week_num==4:
    print("4 is Thursday")
elif week_num==5:
    print("5 is Friday")
elif week_num==6:
    print("6 is Saturday")
elif week_num==7:
    print("7 is Sunday")
else:
    print("Invalid Input")